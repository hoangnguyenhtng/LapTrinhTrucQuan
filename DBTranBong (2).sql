create or alter trigger SoTheCT on TRANDAU_THE 
after insert,update
as 
begin
if trigger_nestlevel() > 1 return
declare @matrandau nvarchar(10), @mact nvarchar(10), @the nvarchar(10),@madoi nvarchar(10),@madn nvarchar(10),@madk nvarchar(10)
