create or alter trigger SobanCT on TRANDAU_BANTHANG
after insert,update
as
begin
if trigger_nestlevel() > 1 return
declare @macauthu nvarchar(20),@matrandau nvarchar(20),@madoi nvarchar(20),@madnha nvarchar(20),@madkhach nvarchar(20)
select @macauthu = MACAUTHU,@matrandau = MATRANDAU from inserted
select @madoi = MADOI from CAUTHU where MACAUTHU = @macauthu
select @madnha =MADOINHA,@madkhach = MADOIKHACH from TRANDAU where TRANDAU.MATRANDAU = @matrandau
update CAUTHU set SOBANTHANG = isnull(SOBANTHANG,0) + 1 where CAUTHU.MACAUTHU = @macauthu
if(@madoi = @madnha)
begin
update TRANDAU set SOBANTHANGDOINHA = isnull(SOBANTHANGDOINHA,0) + 1 where MATRANDAU = @matrandau
update DOIBONG set SOBANTHANG = isnull(SOBANTHANG,0) + 1 where MADOI = @madnha
update DOIBONG set SOBANTHUA = isnull(SOBANTHUA,0) + 1 where MADOI = @madkhach
end
else 
begin
if(@madoi = @madkhach)
begin
update TRANDAU set SOBANTHANGDOIKHACH = isnull(SOBANTHANGDOIKHACH,0) + 1 where MATRANDAU = @matrandau
update DOIBONG set SOBANTHANG = isnull(SOBANTHANG,0) + 1 where MADOI = @madkhach
update DOIBONG set SOBANTHUA = isnull(SOBANTHUA,0) + 1 where MADOI = @madnha
end
end
end

